package edu.me;


public class PersonForms {
	private String yo;
	private String tu;
	private String el;
	private String nosotros;
	private String vosotros;
	private String ellos;

	public String getYo() {
		return yo;
	}

	public PersonForms setYo(String yo) {
		this.yo = yo;
		return this;
	}

	public String getTu() {
		return tu;
	}

	public PersonForms setTu(String tu) {
		this.tu = tu;
		return this;
	}

	public String getEl() {
		return el;
	}

	public PersonForms setEl(String el) {
		this.el = el;
		return this;
	}

	public String getNosotros() {
		return nosotros;
	}

	public PersonForms setNosotros(String nosotros) {
		this.nosotros = nosotros;
		return this;
	}

	public String getVosotros() {
		return vosotros;
	}

	public PersonForms setVosotros(String vosotros) {
		this.vosotros = vosotros;
		return this;
	}

	public String getEllos() {
		return ellos;
	}

	public PersonForms setEllos(String ellos) {
		this.ellos = ellos;
		return this;
	}
}
